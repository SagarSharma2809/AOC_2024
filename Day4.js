const checkDiagonal = (arr) => {
  const n = arr.length;

  // Extract Primary and Anti-Diagonals
  const primaryDiagonal = [];
  const antiDiagonal = [];

  for (let i = 0; i < n; i++) {
    primaryDiagonal.push(arr[i][i]); // Top-left to bottom-right
    antiDiagonal.push(arr[i][n - i - 1]); // Top-right to bottom-left
  }

  // Helper function to check if a diagonal forms a specific word
  const formsWord = (sequence, word) => sequence.join('') === word;

  // Check if both diagonals form "MAS" or "SAM"
  const isMAS = 
    formsWord(primaryDiagonal, "MAS") || formsWord(primaryDiagonal, "SAM");
  const isSAM = 
    formsWord(antiDiagonal, "MAS") || formsWord(antiDiagonal, "SAM");

  // Return true only if both diagonals form the same word
  return isMAS && isSAM;
};


